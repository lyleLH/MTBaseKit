#import "UIViewController+MTFindViewController.h"

//#import "AppDelegate.h"

@implementation UIViewController (MTFindViewController)

 
+ (UIViewController*)mt_topViewController {
    return [self mt_topViewControllerWithRootViewController:[[[UIApplication sharedApplication] delegate] window].rootViewController];
}

 
+ (UIViewController*)mt_topViewControllerWithRootViewController:(UIViewController*)rootViewController {
    if ([rootViewController isKindOfClass:[UITabBarController class]]) {
        UITabBarController* tabBarController = (UITabBarController*)rootViewController;
        return [self mt_topViewControllerWithRootViewController:tabBarController.selectedViewController];
    } else if ([rootViewController isKindOfClass:[UINavigationController class]]) {
        UINavigationController* navigationController = (UINavigationController*)rootViewController;
        return [self mt_topViewControllerWithRootViewController:navigationController.visibleViewController];
    } else if (rootViewController.presentedViewController) {
        UIViewController* presentedViewController = rootViewController.presentedViewController;
        return [self mt_topViewControllerWithRootViewController:presentedViewController];
    } else {
        return rootViewController;
    }
}

@end
