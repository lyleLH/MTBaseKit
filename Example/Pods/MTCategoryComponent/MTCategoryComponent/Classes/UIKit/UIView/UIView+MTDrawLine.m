#import "UIView+MTDrawLine.h"
#import "UIColor+MTExtension.h"

#define SINGLE_LINE_WIDTH           (1 / [UIScreen mainScreen].scale)   //线条高度
@implementation UIView (MTDrawLine)

- (UIView *)mt_addLineWithX:(CGFloat)x y:(CGFloat)y width:(CGFloat)width {
    UIView *lineView = [[UIView alloc] initWithFrame:CGRectMake(x,y,width, SINGLE_LINE_WIDTH)];
    lineView.backgroundColor = [UIColor mt_colorWithHex:0xeeeeee];
    [self addSubview:lineView];
    return lineView;
}

-(UIView *)mt_addLineWithX:(CGFloat)x y:(CGFloat)y width:(CGFloat)width lineColor:(UIColor *)color{
    UIView *lineView = [[UIView alloc] initWithFrame:CGRectMake(x,y,width, SINGLE_LINE_WIDTH)];
    lineView.backgroundColor = color?color:[UIColor mt_colorWithHex:0xeeeeee];
    [self addSubview:lineView];
    return lineView;
}

- (UIView *)mt_addLineWithX:(CGFloat)x y:(CGFloat)y width:(CGFloat)width height:(CGFloat)height lineColor:(UIColor *)color{
    UIView *lineView = [[UIView alloc] initWithFrame:CGRectMake(x,y,width, height)];
    lineView.backgroundColor = color?color:[UIColor mt_colorWithHex:0xeeeeee];
    [self addSubview:lineView];
    return lineView;
}

 
/// 将实线绘制成虚线
/// @param lineView 传入实线
/// @param lineLength 虚线长度
/// @param lineSpacing 虚线的间距
/// @param lineColor 虚线颜色
+ (void)mt_drawDashLine:(UIView *)lineView lineLength:(int)lineLength lineSpacing:(int)lineSpacing lineColor:(UIColor *)lineColor
{
    CAShapeLayer *shapeLayer = [CAShapeLayer layer];
    [shapeLayer setBounds:lineView.bounds];
    [shapeLayer setPosition:CGPointMake(CGRectGetWidth(lineView.frame) / 2, CGRectGetHeight(lineView.frame))];
    [shapeLayer setFillColor:[UIColor clearColor].CGColor];
    //  设置虚线颜色为blackColor
    [shapeLayer setStrokeColor:lineColor.CGColor];
    //  设置虚线宽度
    [shapeLayer setLineWidth:CGRectGetHeight(lineView.frame)];
    [shapeLayer setLineJoin:kCALineJoinRound];
    //  设置线宽，线间距
    [shapeLayer setLineDashPattern:[NSArray arrayWithObjects:[NSNumber numberWithInt:lineLength], [NSNumber numberWithInt:lineSpacing], nil]];
    //  设置路径
    CGMutablePathRef path = CGPathCreateMutable();
    CGPathMoveToPoint(path, NULL, 0, 0);
    CGPathAddLineToPoint(path, NULL,CGRectGetWidth(lineView.frame), 0);
    [shapeLayer setPath:path];
    CGPathRelease(path);
    //  把绘制好的虚线添加上来
    [lineView.layer addSublayer:shapeLayer];
}

@end
